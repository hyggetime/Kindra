/** `systemInstruction` 으로 전달할 전체 문자열 (N = 실제 첨부 장수 1~5) */
export function buildKindraSystemInstruction(imageCount: number): string {
  const parts = [
    '## Role',
    KINDRA_ROLE,
    '',
    '## Philosophy',
    KINDRA_PHILOSOPHY,
    '',
    '## Guidelines',
    KINDRA_GUIDELINES,
    '',
    TRADITIONAL_DRAWING_FRAME,
    '',
    DEVELOPMENTAL_COGNITIVE_LENS,
    '',
    SEEMHUB_STATISTICAL_LENS,
    '',
    buildMultimodalImageProtocol(imageCount),
  ]
  if (isKindraFewShotEnabled()) {
    parts.push(
      '',
      FEW_SHOT_SCOPE_NOTE,
      '',
      '## Few-shot (톤·구조 기준 — 장수는 위 주의문을 따름)',
      buildFewShotBlock(),
    )
  }
  return parts.join('\n')
}

