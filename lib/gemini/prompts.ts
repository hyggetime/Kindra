const FEW_SHOT_SCOPE_NOTE = `### Few-shot 사용 주의
아래 사례는 **5장이 제출되었을 때**의 톤·구조 레퍼런스입니다. 이번 요청의 첨부 장수가 그보다 적으면, 사례를 따라 **그림 번호를 늘리거나 같은 그림을 반복해 설명하지 마세요.** 실제 첨부 N장만 '그림 1'…'그림 N'으로 다룹니다.`

/**
 * 멀티모달 1~5장 — 모델이 따를 출력 골격 (N = 실제 첨부 장수).
 */
export function buildMultimodalImageProtocol(imageCount: number): string {
  const n = Math.min(5, Math.max(1, Math.floor(imageCount)))
  const crossSectionTitle = n >= 2 ? '### 횡단 패턴 (제출한 그림들을 함께)' : '### 이 한 장에서 읽히는 패턴'
  const crossSectionGuide =
    n >= 2
      ? '- **이 절의 역할**: 여러 장 사이의 **시각적** 반복·대비·일관성(선·색·공간·상징)만 다룹니다. **아이의 마음 이야기·성장 서사**는 여기서 길게 펼치지 말고, 아래 **「그림들이 함께 이야기하는 결」(통합 내러티브)** 에 넘깁니다. 장수를 5장으로 부풀리지 않습니다.'
      : '- **이 절의 역할**: **한 장 안**의 구도·필압·색·상징 등 **눈에 보이는 패턴**만 사실 위주로 씁니다. **정서적 통합 내러티브**는 아래 **「그림들이 함께 이야기하는 결」** 에서 다룹니다. 동일 이미지를 번호만 바꿔 여러 장인 것처럼 서술하지 않습니다.'

  return `## 멀티모달 입력 규칙
- 동일 요청에 첨부된 래스터 이미지는 업로드 순서대로 **그림 1 … 그림 ${n}** 입니다. (**총 ${n}장**)
- 텍스트 설명만 보고 추측하지 말고, **각 이미지의 시각적 사실**을 근거로 씁니다.
- 서술 순서: **개별 관찰(그림별) → ${n >= 2 ? '횡단 비교' : '단일 작품 내 패턴'} → 통합 내러티브 → 부모님께(Hygge)**.

## 출력 마크다운 골격 (반드시 이 순서의 큰 제목 사용)
### 그림별 시각 요약
- 이미지는 이미 첨부되어 있으므로, **각 장마다** 접두로 「그림 k」를 **한 번만** 밝힌 뒤 곧바로 **구도·필압·색·인물/상징** 등 사실 위주로 2~5문장으로 씁니다. 유저 프롬프트에 **그림별 개별 메모**가 있을 수 있는데, 이는 **상황적 맥락** 파악용일 뿐이며 **시각 분석의 1차 근거가 되면 안 됩니다.** 같은 문단·인접 문장 안에 **소근육·표현 운동**(선·채색·세부)과 **인지·표현**(상징·계획·장면 구성) 관찰을 **한두 가지** 넣되, 부모님이 알려 주신 **연령·월령**이 있을 때만 위 '발달·인지 렌즈'의 **기준 지표**를 **짧게** 짝지어 씁니다. 또래 집단 경향(심허브)은 **매 문장마다 꼬리표를 달지 말고**, 필요할 때만 **문장 속에** 스며들게 씁니다. **가짜 통계 숫자**는 금지.
- 「그림 1: …」처럼 **번호만 줄줄이 나열하지 마세요.** N장이면 N개의 문단(또는 문단 묶음)으로 나눕니다.

${crossSectionTitle}
${crossSectionGuide}

### 통합 마음 지도
- **한 줄 요약**(첫 문장 또는 첫 짧은 문단) + **키워드 태그 4~6개**(#한글_스네이크)만 이 절의 **앞부분**에 두세요.
- **리포트 전체에서 #태그 문자열은 이 절에만 허용**입니다. 다른 큰 제목 아래에는 **#** 로 시작하는 단어를 **절대 쓰지 마세요.** (웹에서 태그가 칩으로만 보이도록 하기 위함입니다.)
- 태그로 이미 짚은 성격·경향은 **같은 절 안에서 장황하게 되풀이하지 마세요.**(웹 리포트에서 태그가 별도 강조되므로, 본문은 **한 줄 요약과 태그에 담기지 않은 한두 가지**만 덧붙이면 됩니다.) 'The Core Nature'식의 **두 번째 요약 블록**을 만들지 마세요.

### 여러 각도에서 읽는 마음의 결
- **소제목 쓰기**: DAP·KFD·HTP·LMT·PITR 등 **영문 약어를 소제목 첫머리에 단독으로 두지 마세요.** 「한국어로 읽히는 제목(필요 시 약어)」— 예: 「인물 표현을 통해 본 자아상(DAP)」, 「움직이는 가족 그림에서 보는 관계의 온도(KFD)」, 「집·나무·사람으로 엮어본 환경과 마음의 바탕(HTP)」, 「풍경과 장면이 말해 주는 삶의 태도(LMT)」, 「힘든 상황을 그릴 때 드러나는 대처와 힘(PITR)」. 해당 없으면 이 절은 **시각 패턴·상징**만 한국어 소제목으로 쓰고 약어는 생략합니다. **이론 길게 설명하지 말고**, 자세한 배경은 부모님이 **킨드라「마음을 읽는 도구들」(/tools)** 에서 읽을 수 있다는 **짧은 안내 한 줄**로 족합니다.
- 선과 필압의 흐름(Line · Pressure)
- 화면을 어떻게 쓰는지, 여백과 기저선(Space · Layout — 필요 시 기저선 언급)
- 색이 주는 온도와 밀도(Color · 색채)
- **그림들이 함께 이야기하는 결(Narrative · 통합 내러티브)** — **위의「횡단 패턴」/「이 한 장에서 읽히는 패턴」과 겹치지 않게**: 여기서는 **아이의 마음속 이야기·관계·성장·태도**에 초점을 맞추고, 시각 요소는 **이미 앞에서 말한 것을 되풀이하지 말고** 필요한 만큼만 **징표**로 짚으세요. 이 소제목 아래 문단이 **통합 내러티브의 심장**입니다. **심허브·이론이 맞물리는 핵심 한 번**을 여기 두어도 좋습니다(위 심허브 렌즈의 **직접 출처 1~2회** 규칙과 합치도록).
- (위 프로젝티브 맥락을 쓸 때) 「~검사 결과입니다」체 대신 「미술·표현 심리 이론에서는 이런 점을 ~라고 읽기도 해요」 등 **대화체**를 권장합니다.

### 부모님께 (Hygge Tips)
- 실행 가능한 대화·놀이 제안 3가지

### 한계와 안내
- 비진단·경향 제시 목적 한 문장`
}

